#!/usr/bin/perl

use List::Util qw(shuffle);

sub pickcardsfromfilename {
	$filename = shift;
	$color = $filename; $color =~ s/\.mwDeck//; print(OUT "  \/\/ $color\n");
	open(FILE, $filename) or die $!;
	@cards = <FILE>;
	close FILE;
	
	
	for ($i = 0; $i < scalar(@cards); $i++) {
		if($cards[$i] =~ m(\/\/)) {
			unshift(@a, $i);
		}
		if($cards[$i] !~ m/\w/) {
			unshift(@a, $i);
		}
	}

	foreach $r (@a) {
		delete $cards[$r];
	}
	
	print(OUT "@cards[(shuffle(0..$#cards))[0..$numcards]]");
}

